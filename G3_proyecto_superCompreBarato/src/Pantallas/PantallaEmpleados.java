    private void ButtonAGREGARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAGREGARActionPerformed
        //AGREGAR
        Connection con = null;
        try {
            con = Conexion2.getConnection();
            PreparedStatement ps = con.prepareStatement("INSERT INTO empleados (cedula,nombre,apellido1,apellido2,edad,genero, fechaNacimiento, telefono, lugarresidencia ,mail , puesto,contrasena) VALUES(?,?,?,?,?,?,?,?,?,?,?,?)");

            ps.setString(1, txtCedula.getText());
            ps.setString(2, txtNombre.getText());
            ps.setString(3, txtApellido1.getText());
            ps.setString(4, txtApellido2.getText());
            ps.setString(5, txtEdad.getText());
            ps.setString(6, cbGenero.getSelectedItem().toString());
            ps.setString(7, txtFechaNacimiento.getText());
            ps.setString(8, txtTelefono.getText());
            ps.setString(9, txtResidencia.getText());
            ps.setString(10, txtCorreo.getText());
            ps.setString(11, txtPuesto.getText());
            ps.setString(12, txtContrasena.getText());

            if (ps.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(null, "El registro fue guardado");
                limpiarTxt();

            } else {
                JOptionPane.showMessageDialog(null, "Error en registro");

            }
            limpiarTxt();
            con.close();

        } catch (Exception e) {
            System.out.println(e);
        }
        //-------------------------------------------------------------
    }//GEN-LAST:event_ButtonAGREGARActionPerformed

