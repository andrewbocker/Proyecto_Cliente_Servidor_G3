    private void jButtonBUSCARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBUSCARActionPerformed
        // BUSCAR
        Connection con = null;
        try {
            con = Conexion2.getConnection();
            PreparedStatement ps = con.prepareStatement("SELECT * FROM empleados "
                    + "WHERE cedula=?");
            ps.setString(1, txtCedula.getText());
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                txtCedula.setText(rs.getString("cedula"));
                txtNombre.setText(rs.getString("nombre"));
                txtApellido1.setText(rs.getString("apellido1"));
                txtApellido2.setText(rs.getString("apellido2"));
                txtEdad.setText(rs.getString("edad"));
                cbGenero.setSelectedItem(rs.getString("genero"));
                txtFechaNacimiento.setText(rs.getString("fechaNacimiento"));
                txtTelefono.setText(rs.getString("telefono"));
                txtResidencia.setText(rs.getString("lugarresidencia"));
                txtCorreo.setText(rs.getString("mail"));
                txtPuesto.setText(rs.getString("puesto"));
                txtContrasena.setText(rs.getString("contrasena"));
                                                            
            } else {
                //el else cuando la cedula no coincida
                JOptionPane.showMessageDialog(null, "Cedula no registrada:");
            }
            con.close();

        } catch (Exception e) {
            System.err.println(e);
        }
        
    }//GEN-LAST:event_jButtonBUSCARActionPerformed

