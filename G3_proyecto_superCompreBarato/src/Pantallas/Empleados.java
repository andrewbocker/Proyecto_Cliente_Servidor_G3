    private void ButtonAGREGARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAGREGARActionPerformed
        //AGREGAR
        Connection con = null;
        try {
            con = getConnection();
            PreparedStatement ps = con.prepareStatement("INSERT INTO empleados"
                    + "(cedula,nombre,apellido1,apellido2,edad,lugarresidencia,"
                    + "puesto,contrasena VALUES(?,?,?,?,?,?,?,?)");

            ps.setString(1, jTextFieldCEDULA.getText());
            ps.setString(2, jTextFieldNOMBRE.getText());
            ps.setString(3, jTextFieldAPELLIDO_1.getText());
            ps.setString(4, jTextFieldAPELLIDO_2.getText());
            ps.setString(5, jTextFieldEDAD.getText());
            ps.setString(6, jTextFieldRESIDENCIA.getText());
            ps.setString(7, jTextFieldPUESTO.getText());
            ps.setString(8, jTextFieldCONTRASENIA.getText());

            if (ps.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(null, "El registro fue guardado");
                limpiarTxt();

            } else {
                JOptionPane.showMessageDialog(null, "Error en registro");

            }
            con.close();

        } catch (Exception e) {
            System.out.println(e);
        }
        //-------------------------------------------------------------
    }//GEN-LAST:event_ButtonAGREGARActionPerformed

