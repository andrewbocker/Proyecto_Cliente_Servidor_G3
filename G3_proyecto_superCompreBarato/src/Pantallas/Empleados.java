    private void jButtonBUSCARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBUSCARActionPerformed
        // BUSCAR
        Connection con = null;
        try {
            con = getConnection();
            PreparedStatement ps = con.prepareStatement("SELECT * FROM empleados "
                    + "WHERE cedula=?");
            ps.setString(1, jTextFieldCEDULA.getText());
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                jTextFieldCEDULA.setText(rs.getString("cedula"));
                jTextFieldNOMBRE.setText(rs.getString("nombre"));
                jTextFieldAPELLIDO_1.setText(rs.getString("apellido1"));
                jTextFieldAPELLIDO_2.setText(rs.getString("apellido2"));
                jTextFieldEDAD.setText(rs.getString("edad"));
                jTextFieldRESIDENCIA.setText(rs.getString("lugarresidencia"));
                jTextFieldPUESTO.setText(rs.getString("puesto"));
                jTextFieldCONTRASENIA.setText(rs.getString("contrasena"));
                                                            
            } else {
                //el else cuando la cedula no coincida
                JOptionPane.showMessageDialog(null, "Cedula no registrada:");
            }
            con.close();

        } catch (Exception e) {
            System.err.println(e);
        }
        
    }//GEN-LAST:event_jButtonBUSCARActionPerformed

