    private void btnAgregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregar1ActionPerformed
        Connection con = null;

        try
        {
            con = Conexion2.getConnection();
            PreparedStatement ps = con.prepareStatement("INSERT INTO productos (codigo,nombre,precio, cantidadProducto, descripcion) VALUES (?,?,?,?,?)");
            ps.setString(1, txtCodigoProducto.getText());
            ps.setString(2, txtNombreProducto.getText());
            ps.setString(3, txtPrecioProducto.getText());
            ps.setString(4, txtCantidadProducto.getText());
            ps.setString(5, txtDescripcionProducto.getText());
            

            if(ps.executeUpdate() > 0){

                JOptionPane.showMessageDialog(null, "El producto fue agregado correctamente");

            } else {

                JOptionPane.showMessageDialog(null, "Error al agregar el producto");

            }

            con.close();
            MenuProductos menuProductos = new MenuProductos();
            menuProductos.setVisible(true);
            this.dispose();

        } catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_btnAgregar1ActionPerformed

